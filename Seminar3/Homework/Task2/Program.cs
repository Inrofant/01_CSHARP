// Задайте массив на 10 целых чисел.
// Напишите программу, которая определяет
// количество чётных чисел в массиве.

using System;

class Program
{
    static void Main(string[] args)
    {
        // Задаем массив из 10 целых чисел
        int[] array = { 2, 5, 8, 11, 14, 17, 20, 23, 26, 29 };

        // Переменная для подсчета количества чётных чисел
        int count = 0;

        // Проходим по каждому элементу массива и проверяем, является ли число чётным
        foreach (int num in array)
        {
            if (num % 2 == 0)
            {
                count++; // Увеличиваем счетчик, если число чётное
            }
        }

        // Выводим результат
        Console.WriteLine($"Количество чётных чисел в массиве: {count}");
    }
}
