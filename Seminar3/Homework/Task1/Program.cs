// Задайте одномерный массив из 10 целых
// чисел от 1 до 100. Найдите количество элементов
// массива, значения которых лежат в отрезке [20,90]. 

using System;

class Program
{
    static void Main(string[] args)
    {
        // Задаем массив из 10 целых чисел от 1 до 100
        int[] array = { 12, 45, 78, 90, 23, 6, 87, 32, 19, 55 };

        // Переменная для подсчета количества элементов в заданном диапазоне
        int count = 0;

        // Проходим по каждому элементу массива и проверяем, лежит ли его значение в диапазоне [20, 90]
        foreach (int num in array)
        {
            if (num >= 20 && num <= 90)
            {
                count++; // Увеличиваем счетчик, если значение входит в диапазон
            }
        }

        // Выводим результат
        Console.WriteLine($"Количество элементов массива, значения которых лежат в отрезке [20, 90]: {count}");
    }
}