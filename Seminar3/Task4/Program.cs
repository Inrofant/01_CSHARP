// Дано натуральное трёхзначное число. Создайте массив, состоящий из
// цифр этого числа. Младший разряд числа должен располагаться на 0-
// м индексе массива, старший – на 2-м. 

using System;

class Program
{
    static void Main(string[] args)
    {
        // Введите трехзначное число
        int number = 123;

        // Создаем массив для хранения цифр числа
        int[] digits = new int[3];

        // Получаем цифры числа и сохраняем их в массиве
        digits[0] = number % 10;            // Младший разряд (единицы)
        digits[1] = (number / 10) % 10;     // Средний разряд (десятки)
        digits[2] = number / 100;           // Старший разряд (сотни)

        // Выводим цифры на экран
        Console.Write($"Цифры числа: ");
        foreach (int digit in digits)
        {
            Console.Write($"{digit} ");
        }
    }
}