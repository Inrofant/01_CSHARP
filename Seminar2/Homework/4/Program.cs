// Напишите программу, которая на вход принимает
// натуральное число N, а на выходе показывает его цифры через запятую.

Console.Write("Введите натуральное число N: ");
int N = int.Parse(Console.ReadLine()!);

// Проверяем, что число больше нуля
if (N <= 0)
{
    Console.WriteLine("Число должно быть натуральным.");
    return;
}

// Выводим цифры числа через запятую
Console.Write("Цифры числа через запятую: ");
while (N > 0)
{
    int digit = N % 10; // Получаем последнюю цифру числа
    Console.Write(digit); // Выводим цифру
    N /= 10; // Убираем последнюю цифру числа
    if (N > 0)
    {
    Console.Write(", "); // Выводим запятую, если есть ещё цифры
    }
}
Console.WriteLine();